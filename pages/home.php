	<h3>About Dimaonds</h3>
	<p>A diamond is one of the best-known and most sought-after gemstones. Diamonds have been known to mankind and used as decorative items since ancient times; some of the earliest references can be traced to India.

The hardness of diamond and its high dispersion of light – giving the diamond its characteristic "fire" – make it useful for industrial applications and desirable as jewelry. Diamonds are such a highly traded commodity that multiple organizations have been created for grading and certifying them based on the four Cs, which are carat, cut, color, and clarity. Other characteristics, such as presence or lack of fluorescence, also affect the desirability and thus the value of a diamond used for jewelry.</p>
	<h4>Gemological characteristics</h4>
	<p>Four characteristics known informally as the four Cs are now commonly used as the basic descriptors of diamonds: carat, cut, color, and clarity. Most gem diamonds are traded on the wholesale market based on single values for each of the four Cs; for example knowing that a diamond is rated as 1.5 carats (300 mg), VS2 clarity, F color, excellent cut round brilliant, is enough to reasonably establish an expected price range. More detailed information from within each characteristic is used to determine actual market value for individual stones. Consumers who purchase individual diamonds are often advised to use the four Cs to pick the diamond that is "right" for them.</p>
	<ol>
	<li><h4>Carat</h4><p>The carat weight measures the mass of a diamond. One carat is defined as 200 milligrams (about 0.007 ounce avoirdupois). The point unit—equal to one one-hundredth of a carat (0.01 carat, or 2 mg)—is commonly used for diamonds of less than one carat. All else being equal, the price per carat increases with carat weight, since larger diamonds are both rarer and more desirable for use as gemstones.

The price per carat does not increase linearly with increasing size. Instead, there are sharp jumps around milestone carat weights, as demand is much higher for diamonds weighing just more than a milestone than for those weighing just less. As an example, a 0.99 carat diamond may have a significantly lower price per carat than a comparable 1.01 carat diamond, because of differences in demand.

In the wholesale trade of gem diamonds, carat is often used in denominating lots of diamonds for sale. For example, a buyer may place an order for 100 carats (20 g) of 0.5 carats (100 mg), D–F, VS2-SI1, excellent cut diamonds, indicating a wish to purchase 200 diamonds (100 carats (20 g) total mass) of those approximate characteristics. Because of this, diamond prices (particularly among wholesalers and other industry professionals) are often quoted per carat, rather than per stone.</p></li>
		<li><h4>Clarity</h4><p>Clarity is a measure of internal defects of a diamond called inclusions. Inclusions may be crystals of a foreign material or another diamond crystal, or structural imperfections such as tiny cracks that can appear whitish or cloudy. The number, size, color, relative location, orientation, and visibility of inclusions can all affect the relative clarity of a diamond. The Gemological Institute of America (GIA) and other organizations have developed systems to grade clarity, which are based on those inclusions which are visible to a trained professional when a diamond is viewed under 10x magnification.

Diamonds become increasingly rare when considering higher clarity gradings. Only about 20% of all diamonds mined have a clarity rating high enough for the diamond to be considered appropriate for use as a gemstone; the other 80% are relegated to industrial use. Of that top 20%, a significant portion contains one or more visible inclusions. Those that do not have a visible inclusion are known as "eye-clean" and are preferred by most buyers.

Diamonds are graded by the major societies on a scale ranging from flawless to imperfect.</p></li>
	<li><h4>Color</h4><p>The finest quality as per color grading is totally colorless, which is graded as "D" color diamond across the globe, meaning it is absolutely free from any color. The next grade has a very slight trace of color, which can be observed by any expert diamond valuer/grading laboratory. These are graded as E color or F color diamonds.

Diamonds which show very little traces of color are graded as G or H color diamonds. Slightly colored diamonds are graded as I or J or K color. A diamond can be found in any color in addition to colorless. Some of the colored diamonds such as pink are very rare and are priceless.

Most diamonds used as gemstones are basically transparent with little tint, or white diamonds. The most common impurity, nitrogen, replaces a small proportion of carbon atoms in a diamond's structure and causes a yellowish to brownish tint.[15] This effect is present in almost all white diamonds; in only the rarest diamonds is the coloration from this effect undetectable. The GIA has developed a rating system for color in white diamonds, from "D" to "Z" (with D being "colorless" and Z having a bright yellow coloration), which has been widely adopted in the industry and is universally recognized, superseding several older systems. The GIA system uses a benchmark set of natural diamonds of known color grade, along with standardized and carefully controlled lighting conditions. Diamonds with higher color grades are rarer, in higher demand, and therefore more expensive, than lower color grades. Oddly enough, diamonds graded Z are also rare, and the bright yellow color is also highly valued. Diamonds graded D-F are considered "colorless", G-J are considered "near-colorless", K-M are "slightly colored". N-Y usually appear light yellow or brown.</p></li>
		<li><h4>Cut</h4><p>The cut of a diamond describes the manner in which a diamond has been shaped and polished from its beginning form as a rough stone to its final gem proportions. The cut of a diamond describes the quality of workmanship and the angles to which a diamond is cut. There are mathematical guidelines for the angles and length ratios at which the diamond is supposed to be cut in order to reflect the maximum amount of light. Round brilliant diamonds, the most common, are guided by these specific guidelines, though fancy cut stones are not able to be as accurately guided by mathematical specifics.

The techniques for cutting diamonds have been developed over hundreds of years, with perhaps the greatest achievements made in 1919 by mathematician and gem enthusiast Marcel Tolkowsky. He developed the round brilliant cut by calculating the ideal shape to return and scatter light when a diamond is viewed from above. The modern round brilliant has 57 facets (polished faces), counting 33 on the crown (the top half), and 24 on the pavilion (the lower half). The girdle is the thin middle part. The function of the crown is to refract light into various colors and the pavilion's function to reflect light back through the top of the diamond.

Tolkowsky's calculations included some approximations. He calculated the ideal dimensions as:
<ul><li>Table percentage (corner-to-corner diameter of the table divided by overall diameter) = 53%</li>
	<li>Depth percentage (overall depth divided by overall diameter) = 59.3% (not including adjustments for the culet height and girdle thickness)</li>
	<li>Pavilion Angle (angle between the girdle and the pavilion main facets) = 40.75°</li>
	<li>Crown Angle (angle between the girdle and the crown's kite facets) = 34.5°</li>
	<li>Pavilion Depth (depth of pavilion divided by overall diameter) = 43.1%</li>
	<li>Crown Depth (depth of crown divided by overall diameter) = 16.2%</li>
			</ul>
	The culet is the tiny point or facet at the bottom of the diamond. This should be a negligible diameter, otherwise light leaks out of the bottom. Tolkowsky's calculations included neither a culet nor a girdle. However, a girdle is required in reality in order to prevent the diamond from easily chipping in the setting. The thick part of the girdle is normally about 1.7% (of the overall diameter) thicker than the thin part of the girdle.

The further the diamond's characteristics are from the Tolkowsky's ideal, the less light will be reflected. However, there is a small range in which the diamond can be considered "ideal". Tolkowsky's calculations can be repeated for a narrow range of pavilion angles. Such calculations show a slightly larger table percentage, and a trade-off between pavilion angle and crown angle.
</p></li>
